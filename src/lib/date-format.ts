const DEFAULT_TIME_ZONE = "Asia/Shanghai";

const defaultDateTimeFormatter = new Intl.DateTimeFormat("zh-CN", {
  year: "numeric",
  month: "2-digit",
  day: "2-digit",
  hour: "2-digit",
  minute: "2-digit",
  hour12: false,
  timeZone: DEFAULT_TIME_ZONE,
});

export function formatDateTimeCn(value: string | Date | null | undefined, timeZone = DEFAULT_TIME_ZONE): string {
  if (!value) {
    return "-";
  }

  const date = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "-";
  }

  if (timeZone === DEFAULT_TIME_ZONE) {
    return defaultDateTimeFormatter.format(date);
  }

  return new Intl.DateTimeFormat("zh-CN", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
    timeZone,
  }).format(date);
}
